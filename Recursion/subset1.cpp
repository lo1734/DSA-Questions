class Solution {
  public:
    void fun(int idx,int sum,vector<int> &arr,vector<int> &res){
        if(idx>=arr.size()){
            res.push_back(sum);
            return;
        }
        
        sum+=arr[idx];
        fun(idx+1,sum,arr,res);
        sum-=arr[idx];
        fun(idx+1,sum,arr,res);
    }
    vector<int> subsetSums(vector<int>& arr) {
        // code here
        vector<int> res;
        fun(0,0,arr,res);
        return res;
    }
};

